// 初始化引擎
//=======================================================
var e = new D2D_Engine("EMXCanvas",80,800,600);


//=======================================================
// 屏幕更新
//=======================================================
e.update = function(dt) {


};

//=======================================================
// 屏幕渲染
//=======================================================
e.draw = function(dt) {
    		  
    //开始渲染
    this.beginDraw();
    this.clear("#FFFFFF");

    //画线
    this.drawLine(50, 50, 750, 50, "#FF0000");
    this.drawLine(50,100, 750,100, "#00FF00",5);
    
    //画线框矩形
    this.drawStrokeRect(100, 150, 100, 100, "#0000FF");
    this.drawStrokeRect(300, 150, 150, 100, "#00FFFF", 3);
    
    //画填充矩形
    this.drawFillRect(300, 300, 200, 200, "#000000");
    
    //结束渲染
    this.endDraw();

};

//=======================================================
// 启动引擎
//=======================================================
startEngine(e);

















